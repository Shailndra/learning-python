# find year by month number

def year(number):
    if number == 1:
        print("Month is Janumary")
    elif number == 2:
        print("Month is February")
    elif number == 3:
        print("Month is March")
    elif number == 4:
        print("Month is April")
    elif number == 5:
        print("Month is May")
    elif number == 6:
        print("Month is June")
    elif number == 7:
        print("Month is July")
    elif number == 8:
        print("Month is August")
    elif number == 9:
        print("Month is September")
    elif number == 10:
        print("Month is October")
    elif number == 11:
        print("Month is November")
    else:
        print("Month is December")

year(2)